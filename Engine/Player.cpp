Player::Player()
	   :
		random(seed()),
		fitx(0, Graphics::bottomright.x - res.x),
		fity(0, Graphics::bottomright.y - res.y)
{
	pos.x = fitx(random);
	pos.y = fity(random);
	static int i = 0;//第一次为0，第二次才会改变值//静态局部变量
	id = i;
	i++;
}

void Player::clampScreen(const Vec2&min,const Vec2&max)
{
	pos.clamp(min,max);
}

 void Player::PlayerA(const Keyboard& kbd)
 {
	if( kbd.KeyIsPressed(VK_UP)) pos.y -= v.y;
	if (kbd.KeyIsPressed(VK_DOWN)) pos.y += v.y;
	if (kbd.KeyIsPressed(VK_LEFT)) pos.x -= v.x;
	if (kbd.KeyIsPressed(VK_RIGHT)) pos.x += v.x;
 }

 void Player::PlayerB(const Keyboard & kbd)
 {
	 if (kbd.KeyIsPressed('W')) pos.y -= v.y;
	 if (kbd.KeyIsPressed('S')) pos.y += v.y;
	 if (kbd.KeyIsPressed('A')) pos.x -= v.x;
	 if (kbd.KeyIsPressed('D')) pos.x += v.x;
 }

 void Player::scoring(const poo & poo)
 {
	 if (poo.getEaterId()== id)
		 score++;
 }


 

